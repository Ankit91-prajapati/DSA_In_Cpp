#include<iostream>
using namespace std;
int main(){
    int x=4;
    //post increment
    cout<<x<<endl;
    cout<<x++<<endl;
    cout<<x<<endl;
//pre increment
int y=6;
    cout<<y<<endl;
    cout<<++y<<endl;
    cout<<y<<endl;


//method-2
    int a=5;
    //post increment
    cout<<a<<endl;
    a++;
    cout<<a<<endl;
    a--;
    cout<<a<<endl;
//pre increment
int b=7;
    cout<<b<<endl;
    ++b;
    cout<<b<<endl;
    --b;
    cout<<b<<endl;

    return 0;
}

    